import argparse
import textwrap


def print_help() -> None:
    """Выводит справочную информацию о игре."""
    help_text = """
    Игра 'Точки' - это стратегическая игра для двух игроков.

    Основные правила:
    - Игроки по очереди ставят точки своего цвета на поле
    - При соединении точек образуются линии
    - Захваченные области подсчитываются как очки
    - Побеждает игрок с наибольшим количеством очков

    Управление:
    - ЛКМ - поставить точку
    - Кнопки в правой панели - выбор режима игры
    - Можно выбрать размер поля (до 30x33)
    - Есть три режима игры:
      * PVP - игрок против игрока
      * PVAIR - игрок против ИИ (случайные ходы)
      * PVAIS - игрок против умного ИИ

    Таблица лидеров:
    - Сохраняются лучшие результаты
    - Топ-3 игроков отображается в правой панели
    """
    print(textwrap.dedent(help_text).strip())


def main() -> None:
    parser = argparse.ArgumentParser(description='Справка по игре "Точки"')
    parser.add_argument('command', nargs='?', default='help', help='Команда справки')
    args = parser.parse_args()

    if args.command == 'help':
        print_help()
    else:
        print(f"Неизвестная команда: {args.command}")
        print("Доступные команды: help")


if __name__ == "__main__":
    main()
